# agents/multi_dataset.py — Multi-Dataset Comparison Agent

import pandas as pd
import numpy as np
import plotly.graph_objects as go
from plotly.subplots import make_subplots


def compare_datasets(df1: pd.DataFrame, df2: pd.DataFrame,
                      name1: str = "Dataset 1",
                      name2: str = "Dataset 2") -> dict:
    """
    Compares two datasets across multiple dimensions.
    Returns a comparison dict with findings and chart figures.
    """
    results = {
        "name1": name1, "name2": name2,
        "shape": {
            name1: {"rows": df1.shape[0], "cols": df1.shape[1]},
            name2: {"rows": df2.shape[0], "cols": df2.shape[1]},
        },
        "common_columns": [],
        "only_in_1": [],
        "only_in_2": [],
        "missing_comparison": {},
        "stats_comparison": {},
        "distribution_charts": [],
        "summary": [],
    }

    # Column overlap
    cols1 = set(df1.columns.str.lower().str.strip())
    cols2 = set(df2.columns.str.lower().str.strip())
    results["common_columns"] = sorted(cols1 & cols2)
    results["only_in_1"] = sorted(cols1 - cols2)
    results["only_in_2"] = sorted(cols2 - cols1)

    # Missing values
    results["missing_comparison"] = {
        name1: int(df1.isnull().sum().sum()),
        name2: int(df2.isnull().sum().sum()),
    }

    # Stats comparison for common numeric columns
    num1 = df1.select_dtypes(include=[np.number])
    num2 = df2.select_dtypes(include=[np.number])
    common_num = [c for c in num1.columns if c in num2.columns]

    for col in common_num[:6]:
        results["stats_comparison"][col] = {
            name1: {"mean": round(float(df1[col].mean()), 4),
                    "std":  round(float(df1[col].std()),  4),
                    "median": round(float(df1[col].median()), 4)},
            name2: {"mean": round(float(df2[col].mean()), 4),
                    "std":  round(float(df2[col].std()),  4),
                    "median": round(float(df2[col].median()), 4)},
        }

    # Distribution comparison charts
    TEAL, BLUE = "#02C39A", "#3B82F6"
    for col in common_num[:3]:
        fig = go.Figure()
        fig.add_trace(go.Histogram(x=df1[col].dropna(), name=name1,
                                    marker_color=TEAL, opacity=0.7, nbinsx=30))
        fig.add_trace(go.Histogram(x=df2[col].dropna(), name=name2,
                                    marker_color=BLUE, opacity=0.7, nbinsx=30))
        fig.update_layout(
            title=f"Distribution Comparison: {col}",
            barmode="overlay",
            plot_bgcolor="white", paper_bgcolor="white",
            legend=dict(orientation="h", y=1.1)
        )
        results["distribution_charts"].append({"title": col, "fig": fig})

    # Summary insights
    if df1.shape[0] > df2.shape[0]:
        results["summary"].append(f"{name1} has more rows ({df1.shape[0]:,} vs {df2.shape[0]:,})")
    else:
        results["summary"].append(f"{name2} has more rows ({df2.shape[0]:,} vs {df1.shape[0]:,})")

    miss1 = results["missing_comparison"][name1]
    miss2 = results["missing_comparison"][name2]
    if miss1 < miss2:
        results["summary"].append(f"{name1} is cleaner ({miss1} missing vs {miss2})")
    elif miss2 < miss1:
        results["summary"].append(f"{name2} is cleaner ({miss2} missing vs {miss1})")
    else:
        results["summary"].append("Both datasets have the same number of missing values")

    results["summary"].append(
        f"{len(results['common_columns'])} columns in common, "
        f"{len(results['only_in_1'])} only in {name1}, "
        f"{len(results['only_in_2'])} only in {name2}"
    )

    return results
