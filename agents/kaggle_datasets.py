def recommend_datasets(learning_goal: str = "classification",
                        level: str = "beginner") -> list[dict]:
    """
    Recommends datasets based on learning goal and student level.
    """
    goal_lower = learning_goal.lower()

    # Match goal to category
    category = "classification"
    if "regress" in goal_lower or "price" in goal_lower or "predict number" in goal_lower:
        category = "regression"
    elif "cluster" in goal_lower or "segment" in goal_lower or "group" in goal_lower:
        category = "clustering"
    elif "time" in goal_lower or "series" in goal_lower or "forecast" in goal_lower:
        category = "time_series"
    elif "education" in goal_lower or "student" in goal_lower or "school" in goal_lower:
        category = "education"

    datasets = CURATED_DATASETS.get(category, CURATED_DATASETS["classification"])

    # Filter by level
    if level == "beginner":
        datasets = [d for d in datasets if d["difficulty"] == "Beginner"]

    return datasets


def search_datasets_live(query: str, kaggle_api_key: str = None) -> list[dict]:
    """
    Searches Kaggle API for datasets matching the query.
    Requires Kaggle API credentials.
    Falls back to curated list if no API key.
    """
    if not kaggle_api_key:
        return recommend_datasets(query)
    try:
        import kaggle
        datasets = kaggle.api.dataset_list(search=query, sort_by="votes")
        return [{"name": d.title, "rows": "Unknown", "cols": "Unknown",
                 "description": getattr(d, "subtitle", "No description"),
                 "url": f"https://kaggle.com/datasets/{d.ref}",
                 "difficulty": "Unknown", "topic": query} for d in datasets[:6]]
    except Exception:
        return recommend_datasets(query)


def render_dataset_cards(st, datasets: list[dict]) -> None:
    """Renders dataset recommendation cards in Streamlit."""
    for i, ds in enumerate(datasets):
        with st.container():
            col1, col2 = st.columns([4, 1])
            with col1:
                st.markdown(f"**{ds['name']}**")
                st.caption(f"📊 {ds['rows']} rows · {ds['cols']} columns · "
                           f"🎯 {ds['difficulty']} · 📁 {ds['topic']}")
                st.markdown(f"_{ds['description']}_")
            with col2:
                st.link_button("View on Kaggle", ds["url"])
            if i < len(datasets) - 1:
                st.divider()